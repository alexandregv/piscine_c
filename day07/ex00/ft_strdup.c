#include <stdlib.h>

char	*ft_strdup(char *src)
{
	int		i;
	int		size;
	char	*dest;

	size = 0;
	while (src[size] != '\0')
		size++;
	dest = (char*)malloc(sizeof(*dest) * (size + 1));
	i = 0;
	while (i < size)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
