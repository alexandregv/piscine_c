#include "ft.h"

int		add(int x, int y)
{
	return (x + y);
}

int		subtract(int x, int y)
{
	return (x - y);
}

int		multiply(int x, int y)
{
	return (x * y);
}

int		divide(int x, int y)
{
	return (x / y);
}

int		modulo(int x, int y)
{
	return (x % y);
}
